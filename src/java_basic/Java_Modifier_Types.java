package java_basic;

/*
 * Modifiers are keywords that you add to those definitions to change their meanings
 * Java Access Modifiers
 * Non Access Modifiers
 */

/*
 * Access Control Modifiers
 * set access levels for classes, variables, methods, constructors
 * NO modifiers - Visible to the package, the default
 * private - Visible to the class only 
 * public - Visible to the world 
 * protected - Visible to the package and all subclasses
 */

/*
 * Non-Access Control Modifiers
 * to achieve many other functionality
 * static modifier - creating class methods and variables
 * final modifier - finalizing the implementations of classes, methods, variables
 * abstract modifier - creating abstract classes and methods
 * synchronized and volatile modifiers - using for threads
 */

public class Java_Modifier_Types {
	private boolean myFlag;
	static final double weeks = 9.5;
	protected static final int BOXWIDTH = 42;
	

}
