package java_basic;

public class Java_While_Loop {

	public static void main(String[] args) {
		
		int i = 1;
		while(i <= 10) {
			System.out.println(i);
			i++;
		}
		
		System.out.println("=======Infinitive While loop========");
		while(true) {
			System.out.println("infinitive while loop");
		}
	}

}
